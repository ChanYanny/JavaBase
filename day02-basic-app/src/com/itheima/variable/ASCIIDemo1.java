public class ASCIIDemo1 {
    public static void main(String[] args) {
        System.out.println('a' + 10);
        System.out.println('A' + 10);
        System.out.println('0' + 10);

        int a = 0b01100001;
        System.out.println(a);

        int b = 0x61;
        System.out.println(b);

        int c = 061;
        System.out.println(c);
    }
}

